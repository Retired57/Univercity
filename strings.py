# Практическое задание по уроку "Строки и индексация строк"

example = "Топинамбур"

print(example[0])                      # первая буква
print(example[-1])                     # последняя буква
print(example[(len(example) // 2):])   # вторая половина слова
print(example[::-1])                   # слово наоборот
print(example[1::2])                   # каждая вторая буква
